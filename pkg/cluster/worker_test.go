// --- Lifecycle ---

// workerLifecycleOnCall handles both add and remove flows in sequence.
// The cluster starts with controller + compute-0. WorkerAdd creates compute-1,
// then WorkerRemove deletes compute-1.
func workerLifecycleOnCall(t *testing.T) func([]string, string) docker.MockResult {
	t.Helper()

	nodesConf := "# Generated by sind\n" +
		"NodeName=compute-0 CPUs=2 RealMemory=2048 State=UNKNOWN\n" +
		"PartitionName=all Nodes=compute-0 Default=YES MaxTime=INFINITE State=UP\n"

	// Track whether compute-1 has been added so remove can include it.
	added := false

	return func(args []string, stdin string) docker.MockResult {
		if len(args) == 0 {
			return docker.MockResult{}
		}
		joined := strings.Join(args, " ")

		switch {
		case args[0] == "ps":
			if added {
				return docker.MockResult{Stdout: workerContainers("compute-0", "compute-1")}
			}
			return docker.MockResult{Stdout: workerContainers("compute-0")}

		case args[0] == "inspect" && args[1] == "sind-dns":
			return docker.MockResult{Stdout: inspectJSON(t, "sind-dns", "running", map[docker.NetworkName]string{
				"sind-mesh": "10.0.0.2",
			})}

		case args[0] == "inspect" && args[1] == "sind-dev-controller":
			return docker.MockResult{Stdout: inspectJSONLabels(t, "sind-dev-controller", "running",
				map[docker.NetworkName]string{"sind-dev-net": "10.0.1.1"},
				map[string]string{"sind.slurm.version": "25.11.0"})}

		case args[0] == "inspect" && strings.HasPrefix(args[1], "sind-dev-compute-"):
			return docker.MockResult{Stdout: inspectJSON(t, args[1], "running", map[docker.NetworkName]string{
				"sind-dev-net": "10.0.1.3",
			})}

		case args[0] == "exec" && args[1] == "sind-ssh" && len(args) > 2 && args[2] == "cat":
			// ReadFile: SSH pubkey
			return docker.MockResult{Stdout: "ssh-ed25519 AAAA-test-key\n"}

		case args[0] == "exec" && args[1] == "sind-dev-controller" && len(args) > 2 && args[2] == "cat":
			// ReadFile: sind-nodes.conf
			return docker.MockResult{Stdout: nodesConf}

		case args[0] == "exec" && args[1] == "-i" && strings.Contains(joined, "sind-dev-controller"):
			// WriteFile: updated sind-nodes.conf
			return docker.MockResult{}

		case args[0] == "exec" && args[1] == "sind-dev-controller" && len(args) > 2 && args[2] == "scontrol":
			return docker.MockResult{}

		case args[0] == "create":
			added = true
			return docker.MockResult{Stdout: "new-cid\n"}

		case args[0] == "network" && args[1] == "connect":
			return docker.MockResult{}

		case args[0] == "start":
			return docker.MockResult{}

		case args[0] == "exec":
			container := args[1]
			if container == "-i" {
				return docker.MockResult{}
			}
			if len(args) > 2 {
				switch cmd := args[2]; {
				case cmd == "sh" && strings.Contains(joined, "is-system-running"):
					return docker.MockResult{Stdout: "running\n"}
				case cmd == "bash" && strings.Contains(joined, "/dev/tcp"):
					return docker.MockResult{Stdout: "SSH-2.0-OpenSSH_9.0\n"}
				case cmd == "mkdir":
					return docker.MockResult{}
				case cmd == "ssh-keyscan":
					return docker.MockResult{Stdout: "localhost ssh-ed25519 AAAA-hostkey-" + container + "\n"}
				case cmd == "systemctl" && len(args) > 3 && args[3] == "enable":
					return docker.MockResult{}
				case cmd == "systemctl" && len(args) > 3 && args[3] == "is-active":
					return docker.MockResult{Stdout: "active\n"}
				}
			}
			return docker.MockResult{}

		case args[0] == "cp":
			if len(args) == 3 && args[2] == "-" && strings.Contains(args[1], "sind-dns") {
				return docker.MockResult{Stdout: emptyCorefileTar()}
			}
			return docker.MockResult{}

		case args[0] == "kill":
			return docker.MockResult{}

		case args[0] == "stop":
			return docker.MockResult{}

		case args[0] == "rm":
			return docker.MockResult{}
		}

		t.Logf("unhandled lifecycle mock call: %v", args)
		return docker.MockResult{}
	}
}

func TestWorkerAddRemoveLifecycle(t *testing.T) {
	var m docker.MockExecutor
	m.OnCall = workerLifecycleOnCall(t)
	client := docker.NewClient(&m)
	mgr := mesh.NewManager(client)

	ctx, cancel := context.WithTimeout(t.Context(), 10*time.Second)
	defer cancel()

	// Add a managed worker.
	nodes, err := WorkerAdd(ctx, client, mgr, WorkerAddOptions{
		ClusterName: "dev",
		Count:       1,
		CPUs:        2,
		Memory:      "2g",
		TmpSize:     "1g",
	}, time.Millisecond)

	require.NoError(t, err)
	require.Len(t, nodes, 1)
	assert.Equal(t, "compute-1", nodes[0].Name)
	assert.Equal(t, "compute", nodes[0].Role)
	assert.Equal(t, StatusRunning, nodes[0].Status)

	// Remove the worker.
	err = WorkerRemove(ctx, client, mgr, "dev", []string{"compute-1"})
	require.NoError(t, err)

	// Verify both add and remove operations happened.
	var created, stopped, removed bool
	for _, call := range m.Calls {
		args := call.Args
		joined := strings.Join(args, " ")
		if args[0] == "create" && strings.Contains(joined, "sind-dev-compute-1") {
			created = true
		}
		if args[0] == "stop" && strings.Contains(joined, "compute-1") {
			stopped = true
		}
		if args[0] == "rm" && strings.Contains(joined, "compute-1") {
			removed = true
		}
	}
	assert.True(t, created, "compute-1 should be created during add")
	assert.True(t, stopped, "compute-1 should be stopped during remove")
	assert.True(t, removed, "compute-1 should be removed during remove")
}
