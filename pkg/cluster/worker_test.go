// workerRemoveOnCall returns a mock OnCall handler for WorkerRemove tests.
// The cluster has controller + compute-0 + compute-1, removing compute-1.
func workerRemoveOnCall(t *testing.T, nodesConf string) func([]string, string) docker.MockResult {
	t.Helper()

	return func(args []string, stdin string) docker.MockResult {
		if len(args) == 0 {
			return docker.MockResult{}
		}
		joined := strings.Join(args, " ")

		switch {
		// ListContainers
		case args[0] == "ps":
			return docker.MockResult{Stdout: workerContainers("compute-0", "compute-1")}

		// ReadFile: sind-nodes.conf
		case args[0] == "exec" && args[1] == "sind-dev-controller" && len(args) > 2 && args[2] == "cat":
			if nodesConf == "" {
				return docker.MockResult{Err: fmt.Errorf("No such file")}
			}
			return docker.MockResult{Stdout: nodesConf}

		// WriteFile: updated sind-nodes.conf
		case args[0] == "exec" && args[1] == "-i" && strings.Contains(joined, "sind-dev-controller"):
			return docker.MockResult{}

		// scontrol reconfigure
		case args[0] == "exec" && args[1] == "sind-dev-controller" && len(args) > 2 && args[2] == "scontrol":
			return docker.MockResult{}

		// DNS: CopyFromContainer (read Corefile)
		case args[0] == "cp" && len(args) == 3 && args[2] == "-" && strings.Contains(args[1], "sind-dns"):
			return docker.MockResult{Stdout: emptyCorefileTar()}

		// DNS: CopyToContainer / signal
		case args[0] == "cp":
			return docker.MockResult{}
		case args[0] == "kill":
			return docker.MockResult{}

		// Known hosts: exec on sind-ssh
		case args[0] == "exec" && args[1] == "sind-ssh":
			return docker.MockResult{Stdout: "compute-1.dev.sind.local ssh-ed25519 AAAA\n"}
		case args[0] == "exec" && args[1] == "-i":
			return docker.MockResult{}

		// Stop + remove containers
		case args[0] == "stop":
			return docker.MockResult{}
		case args[0] == "rm":
			return docker.MockResult{}
		}

		t.Logf("unhandled worker remove mock call: %v", args)
		return docker.MockResult{}
	}
}

func TestWorkerRemove_Managed(t *testing.T) {
	nodesConf := "# Generated by sind\n" +
		"NodeName=compute-0 CPUs=2 RealMemory=2048 State=UNKNOWN\n" +
		"NodeName=compute-1 CPUs=2 RealMemory=2048 State=UNKNOWN\n" +
		"PartitionName=all Nodes=compute-0,compute-1 Default=YES MaxTime=INFINITE State=UP\n"

	var m docker.MockExecutor
	m.OnCall = workerRemoveOnCall(t, nodesConf)
	client := docker.NewClient(&m)
	mgr := mesh.NewManager(client)

	err := WorkerRemove(t.Context(), client, mgr, "dev", []string{"compute-1"})

	require.NoError(t, err)

	// Verify key operations.
	var scontrolCalled, containerStopped, containerRemoved bool
	var writeStdin string
	for _, call := range m.Calls {
		args := call.Args
		joined := strings.Join(args, " ")
		if args[0] == "exec" && args[1] == "-i" && strings.Contains(joined, "sind-dev-controller") {
			writeStdin = call.Stdin
		}
		if args[0] == "exec" && args[1] == "sind-dev-controller" && len(args) > 2 && args[2] == "scontrol" {
			scontrolCalled = true
		}
		if args[0] == "stop" && strings.Contains(joined, "compute-1") {
			containerStopped = true
		}
		if args[0] == "rm" && strings.Contains(joined, "compute-1") {
			containerRemoved = true
		}
	}

	assert.True(t, scontrolCalled, "scontrol reconfigure should be called")
	assert.True(t, containerStopped, "container should be stopped")
	assert.True(t, containerRemoved, "container should be removed")
	assert.Contains(t, writeStdin, "compute-0", "updated conf should still have compute-0")
	assert.NotContains(t, writeStdin, "compute-1", "updated conf should not have compute-1")
}

