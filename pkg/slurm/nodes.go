package slurm

import (
	"fmt"
	"strconv"
	"strings"

	"github.com/GSI-HPC/sind/pkg/config"
)

// GenerateNodesConf generates the sind-nodes.conf content with NodeName and
// PartitionName directives. Only managed compute nodes are included.
// Non-compute nodes and unmanaged compute nodes are skipped.
func GenerateNodesConf(nodes []config.Node) string {
	var b strings.Builder
	var managed []string
	idx := 0

	b.WriteString("# Generated by sind\n")

	for _, n := range nodes {
		if n.Role != "compute" {
			continue
		}

		count := n.Count
		if count <= 0 {
			count = 1
		}

		memMB, _ := parseMemoryMB(n.Memory)

		for i := 0; i < count; i++ {
			name := fmt.Sprintf("compute-%d", idx)
			idx++

			if n.Managed != nil && !*n.Managed {
				continue
			}

			b.WriteString(fmt.Sprintf("NodeName=%s CPUs=%d RealMemory=%d State=UNKNOWN\n",
				name, n.CPUs, memMB))
			managed = append(managed, name)
		}
	}

	if len(managed) > 0 {
		b.WriteString(fmt.Sprintf("PartitionName=all Nodes=%s Default=YES MaxTime=INFINITE State=UP\n",
			strings.Join(managed, ",")))
	}

	return b.String()
}

// NodeEntry represents a single node to add to sind-nodes.conf.
type NodeEntry struct {
	Name     string
	CPUs     int
	MemoryMB int
}

// AddNodesToConf adds node definitions to existing sind-nodes.conf content.
// New NodeName lines are inserted and the PartitionName Nodes= list is
// updated to include both existing and new nodes.
func AddNodesToConf(existing string, nodes []NodeEntry) string {
	lines := strings.Split(strings.TrimRight(existing, "\n"), "\n")

	var managed []string
	var kept []string

	for _, line := range lines {
		if strings.HasPrefix(line, "PartitionName=") {
			continue // will be regenerated
		}
		kept = append(kept, line)
		if strings.HasPrefix(line, "NodeName=") {
			fields := strings.Fields(line)
			if len(fields) > 0 {
				name := strings.TrimPrefix(fields[0], "NodeName=")
				managed = append(managed, name)
			}
		}
	}

	for _, n := range nodes {
		kept = append(kept, fmt.Sprintf("NodeName=%s CPUs=%d RealMemory=%d State=UNKNOWN",
			n.Name, n.CPUs, n.MemoryMB))
		managed = append(managed, n.Name)
	}

	if len(managed) > 0 {
		kept = append(kept, fmt.Sprintf("PartitionName=all Nodes=%s Default=YES MaxTime=INFINITE State=UP",
			strings.Join(managed, ",")))
	}

	return strings.Join(kept, "\n") + "\n"
}

// RemoveNodesFromConf removes the named nodes from existing sind-nodes.conf
// content. NodeName lines for the removed nodes are deleted and the
// PartitionName Nodes= list is updated.
func RemoveNodesFromConf(existing string, names []string) string {
	remove := make(map[string]bool, len(names))
	for _, n := range names {
		remove[n] = true
	}

	lines := strings.Split(strings.TrimRight(existing, "\n"), "\n")

	var managed []string
	var kept []string

	for _, line := range lines {
		if strings.HasPrefix(line, "PartitionName=") {
			continue // will be regenerated
		}
		if strings.HasPrefix(line, "NodeName=") {
			fields := strings.Fields(line)
			if len(fields) > 0 {
				name := strings.TrimPrefix(fields[0], "NodeName=")
				if remove[name] {
					continue // skip removed node
				}
				managed = append(managed, name)
			}
		}
		kept = append(kept, line)
	}

	if len(managed) > 0 {
		kept = append(kept, fmt.Sprintf("PartitionName=all Nodes=%s Default=YES MaxTime=INFINITE State=UP",
			strings.Join(managed, ",")))
	}

	return strings.Join(kept, "\n") + "\n"
}

// ParseMemoryMB parses a Docker-style memory string (e.g. "2g", "512m") and
// returns the value in megabytes.
func ParseMemoryMB(s string) (int, error) {
	return parseMemoryMB(s)
}

// parseMemoryMB parses a Docker-style memory string (e.g. "2g", "512m") and
// returns the value in megabytes.
func parseMemoryMB(s string) (int, error) {
	if len(s) == 0 {
		return 0, fmt.Errorf("empty memory string")
	}

	suffix := s[len(s)-1]
	numStr := s[:len(s)-1]

	switch suffix {
	case 'g', 'G':
		n, err := strconv.Atoi(numStr)
		if err != nil {
			return 0, fmt.Errorf("invalid memory value: %q", s)
		}
		return n * 1024, nil
	case 'm', 'M':
		n, err := strconv.Atoi(numStr)
		if err != nil {
			return 0, fmt.Errorf("invalid memory value: %q", s)
		}
		return n, nil
	default:
		return 0, fmt.Errorf("unsupported memory suffix in %q, use 'm' or 'g'", s)
	}
}
